/*
 * @param bookPath A path to a biblical book, as defined in the original epub, e.g. "mrk_1.xhtml"
 *
 * @returns Only the id part, e.g. "mrk"
 * */
export const getIdFromBookPath = (bookPath: string) => bookPath.replace(/(_[0-9]*)?\.xhtml/, '')

export const getNewBookTitleForOriginalInlineTitle = (originalInlineTitle: string): string => {
    const bookTitles = biblicalBookTitleMapping.find((titles) => titles.originalInlineTitle === originalInlineTitle)
    if (!bookTitles) {
        throw Error(`Couldn't find new book title for original inline title "${originalInlineTitle}"`)
    }
    return bookTitles.newTitle
}

export const getNewBookTitleForOriginalBookId = (bookId: string): string => {
    const bookTitles = biblicalBookTitleMapping.find((titles) => titles.id === bookId)
    if (!bookTitles) {
        throw Error(`Couldn't find new book title for book id "${bookId}"`)
    }
    return bookTitles.newTitle
}

export const getBiblicalBookNumberByTitle = (bookTitle: string): number => biblicalBookTitleMapping.findIndex((titles) => titles.newTitle === bookTitle) + 1
